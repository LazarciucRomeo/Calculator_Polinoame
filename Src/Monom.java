public class Monom {
    private double coeficient;
    private int putere;

    public Monom(double coeficient, int putere)
    {

        this.coeficient = coeficient;
        this.putere = putere;
    }

    public Monom() {

   }

    public double getPutere() {
        return putere;
    }

    public void setPutere(int putere) {
        this.putere = putere;
    }

    public double getCoeficient() { return coeficient; }

    public void setCoef(float coeficient) {
        this.coeficient = coeficient;
    }}







